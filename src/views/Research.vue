        <h5 class="headerTwo">Research Mode</h5>
        <p class="pText"><!-- eslint-disable max-len -->
        At NARAP’s core is a simple, successful research model: pre-health professional students volunteering as Research Associates (RAs) for one
        4-hour shift per week as data collectors for clinical studies in the emergency department.  RAs facilitate the studies in the manner the
        investigators choose to use them. Depending on their training, this may range from potential participant identification, through reading
        the informed consent and scribing for a professional researcher, to data entry and the details concluding the study. Of course, the
        investigators are responsible for the research; the RAs are there to facilitate.<br><br>
        NARAP studies on the emergency department as a platform to facilitate primary health care screenings follow this model.
        During their weekly shifts, RAs approach as many non-emergent patients and their visitors as possible to ask a detailed history on the
        prevention or screening issue under study. If participants are not up-to-date, RAs offer them ways to correct this, including referrals
        for specific interventions. After a period of time, other RAs make a telephone follow-up call to determine if the intervention has been
        accomplished or scheduled.<br><br>
        RAs carry secure hand-held computer tablets to record data which is transmitted to HIPAA-compliant cloud-based secure web
        application designed exclusively to support data capture for research studies.<br><br>
